    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String asal = jComboBox1.getSelectedItem().toString();
        String tujuan = jComboBox2.getSelectedItem().toString();

        if (asal.equals(tujuan)) {
            JOptionPane.showMessageDialog(rootPane, "Asal dan tujuan tidak boleh sama.");
            return;
        }
        
        ArrayList<Trip> foundTrips = new ArrayList<>();
        for (Trip trip : s.t) {
            if (trip.getAsal().equalsIgnoreCase(asal) && trip.getTujuan().equalsIgnoreCase(tujuan)) {
                foundTrips.add(trip);
            }
        }
        
        if (foundTrips.size() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Tidak ada trip yang tersedia.");
        } else {
            this.dispose();
            new UserTrips(pn, foundTrips).setVisible(true);
        }
            
    }//GEN-LAST:event_jButton5ActionPerformed

