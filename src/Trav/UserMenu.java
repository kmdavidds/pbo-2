    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        ArrayList<String> ordersCode = new ArrayList<>();
        // read from orders.txt
        String fileName = "orders.txt";
        try {
            File file = new File(fileName);
            Scanner scanner = new Scanner(file);
            
            List<String> lines = Files.readAllLines(Paths.get(fileName));
            scanner.close();

            for (int i = 0; i < lines.size(); i++) {
                String line = lines.get(i);
                String[] data = line.split("\\$");
                if (data[0].equals(pn.getId())) {
                    ordersCode.add(data[1]);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        String output = "";
        for (int i = 0; i < ordersCode.size(); i++) {
            output += i+1;
            output += ". ";
            output += ordersCode.get(i);
            output += "\n";
        }

        JOptionPane.showMessageDialog(rootPane, output);
    }//GEN-LAST:event_jButton9ActionPerformed

